# criação da lista de números inteiros com possíveis duplicatas
numeros = [10, 20, 30, 10, 40, 20, 50, 30]

# exibição da lista original
print("Lista original:", numeros)

# remover os números repetidos
numeros_unicos = list(set(numeros))

# exibição da lista resultante com números únicos
print("Lista sem números repetidos:", numeros_unicos)
